public class ProfilePage extends Fragment {

    Button logout_button;
    Button delete_button;
    FirebaseAuth auth;
    private TextView emaiTextView;


    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);

    }

    @SuppressLint("MissingInflatedId")
    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        View view  = inflater.inflate(R.layout.fragment_profile_page, container, false);

        logout_button = view.findViewById(R.id.logout_btn);
        logout_button.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {
                FirebaseAuth.getInstance().signOut();
                Intent intent = new Intent(getActivity(),LogIn.class);
                getActivity().finish();
                startActivity(intent);

            }
        });

        auth = FirebaseAuth.getInstance();
        delete_button = view.findViewById(R.id.delete_btn);
        delete_button.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {
                Intent intent = new Intent(getActivity(),DeleteAccepting.class);
                getActivity().finish();
                startActivity(intent);
            }
        });

        emaiTextView = view.findViewById(R.id.my_email);
        FirebaseUser user = auth.getCurrentUser();
        if (user != null){
            String email = user.getEmail();
            emaiTextView.setText(email);
        }

        return view;
    }
}